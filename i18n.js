export function t(key, lang) {
    const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;
    return dict[key] ?? TRANSLATIONS.en[key] ?? key;
}

// Walks every element with a data-i18n attribute and sets its text
// (or placeholder, for inputs/textareas) to the translated string.
// Also flips document direction for RTL languages (Arabic) — this
// handles text direction correctly but does not mirror the overall
// layout (sidebar side, icon positions); that's a larger visual pass.
export function applyTranslations(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const value = t(key, lang);
        if (el.hasAttribute('data-i18n-placeholder')) {
            el.setAttribute('placeholder', value);
        } else {
            el.textContent = value;
        }
    });
    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('dir', isRTL(lang) ? 'rtl' : 'ltr');
}